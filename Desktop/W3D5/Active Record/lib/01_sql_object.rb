class SQLObject
  def self.columns
    return @columns if @columns
    cols = DBConnection.execute2(<<-SQL).first
      SELECT
        *
      FROM
        #{@table_name}
      LIMIT
        0
    SQL
    cols.map!(&:to_sym)
    @columns = cols
  end

  def self.finalize!
    SQLObject.columns.each do |method|
      define_method(method) do
        SQLObject.attributes[method]
      end
      define_method("#{method}=") do |value|
        SQLObject.attributes[method] = value
      end
    end
  end

